#include <iostream>
#include <cmath>
using namespace std;

/* ============================================================
   ЗАВДАННЯ If33
   ============================================================ */
void taskIf33() 
{
    double x;
    int p;

    cout << "Введіть x: ";
    cin >> x;

    if (x <= 1)
        p = (int)(x * x * x);
    else if (x < 4)
        p = (int)(2 * x + 1);
    else
        p = 9;

    cout << "p(x) = " << p << endl;
}

/* ============================================================
   ЗАВДАННЯ If19
   ============================================================ */
void taskIf19() 
{
    int a, b, c, d;

    cout << "Введіть 4 цілі числа:\n";
    cin >> a >> b >> c >> d;

    if (a != b && b == c && c == d)
        cout << "Відмінне число має номер: 1\n";
    else if (b != a && a == c && c == d)
        cout << "Відмінне число має номер: 2\n";
    else if (c != a && a == b && b == d)
        cout << "Відмінне число має номер: 3\n";
    else
        cout << "Відмінне число має номер: 4\n";
}

/* ============================================================
   ЗАВДАННЯ 2 — ТОЧКА В ФІГУРІ (ВАРІАНТ 13)
   ============================================================ */
void taskPointInFigure() 
{
    double x, y, r;

    cout << "Введіть x, y: ";
    cin >> x >> y;

    cout << "Введіть r: ";
    cin >> r;

    if (x >= 0 && y <= 0 && x * x + y * y <= r * r)
        cout << "Точка потрапляє у фігуру (варіант 13)\n";
    else
        cout << "Точка НЕ потрапляє у фігуру\n";
}

/* ============================================================
   МЕНЮ (ЗАВДАННЯ 3)
   ============================================================ */
int main() 
{
    int choice;

    do 
    {
        cout << "\n===== МЕНЮ =====\n";
        cout << "1 – If33\n";
        cout << "2 – If19\n";
        cout << "3 – Точка у фігурі (варіант 13)\n";
        cout << "0 – Вихід\n";
        cout << "Ваш вибір: ";
        cin >> choice;

        switch (choice) 
        {
        case 1:
            taskIf33();
            break;
        case 2:
            taskIf19();
            break;
        case 3:
            taskPointInFigure();
            break;
        case 0:
            cout << "Завершення програми...\n";
            break;
        default:
            cout << "Невірний вибір\n";
        }
    } 
    while (choice != 0);

    return 0;
}
